
public class Operadores {

	public static void main(String[] args) {
		
		int numero1 = 10;
		int numero2 = 12;
		int soma = numero1 + numero2;
		
		System.out.println("A soma dos números " + numero1 + " e " + numero2 + " é: " + soma);
		
		int minuendo = 27;
		int subtraendo = 12;
		int diferenca = numero1 - numero2;
		
		System.out.println("A diferenda da subtração dos números " + minuendo + " e " + subtraendo + " é: " + diferenca);
		

	}

}
